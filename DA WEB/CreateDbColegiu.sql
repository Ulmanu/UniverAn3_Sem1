if object_id('v_catedre','V') is not null
	drop view v_catedre
go
create view v_catedre
as
	select idcatedre, nmcatedre, catedre.idprofesor,nmprofesor
		from catedre inner join profesor on catedre.idprofesor=profesor.idprofesor
go

if object_id('v_Localit','V') is not null
	drop view v_Localit
go
create view v_Localit
as
	select idlocalit, nmlocalit, localit.idraion,nmraion
		from localit inner join raion on localit.idraion=raion.idraion
go

if object_id('v_Objstud','V') is not null
	drop view v_Objstud
go
