-- Crearea tabelei Catedre
CREATE TABLE Catedre
  ( 
     idcatedre Int PRIMARY KEY
     ,nmcatedre Char(50)UNIQUE
     ,idprofesor Int
  )  
GO
-- Crearea tabelei Objstud
CREATE TABLE objstud
  ( 
     idobjstud Int   PRIMARY KEY
     ,nmobjstud Char(50)UNIQUE
     ,idcatedre Int DEFAULT 1
  )  
GO  
-- Crearea tabelei Profesor
CREATE TABLE profesor
  ( 
     idprofesor Int   PRIMARY KEY
     ,nmprofesor Char(50) UNIQUE
   )
GO
-- Crearea tabelei ObjStGr
CREATE TABLE objstgr
  ( 
      idobjstgr Int IDENTITY  PRIMARY KEY
      ,idgrupa Int
      ,idobjstud Int
      ,idprofesor Int
  )  
GO
-- Crearea tabelei Grupa
CREATE TABLE grupa
  ( 
     idgrupa Int   PRIMARY KEY
     ,nmgrupa Char(30)UNIQUE
     ,anadm Int
     ,idprofesor Int
     ,idspec Int
  )  
GO
-- Crearea tabelei Student
CREATE TABLE student
  ( 
     idstudent Int IDENTITY  PRIMARY KEY
     ,nmstud Char(50)
     ,prenstud Char(50)
     ,patrstud Char(50)
     ,sexstud Char(1) CHECK (sexstud='m' or sexstud='f')
     ,Ncarnet Char(10)
     ,DataNs Date
     ,GSM Char(15)
     ,idgrupa Int
     ,idlocalit Int
  )  
GO  
--* Crearea tabelei Raion
CREATE TABLE raion
  ( 
     idraion Int   PRIMARY KEY
     ,nmraion Char(50)UNIQUE
  )
GO  
--* Crearea tabelei Spec
CREATE TABLE spec
  ( 
     idspec Int   PRIMARY KEY
     ,nmspec Char(50) UNIQUE
  )
GO  
--* Crearea tabelei Localit
CREATE TABLE localit
  ( 
     idlocalit Int   PRIMARY KEY
     ,nmlocalit Char(50) UNIQUE
     ,idraion Int
  )  
GO  
--* Crearea tabelei notele
CREATE TABLE notele
  ( 
     idnotele Int  IDENTITY PRIMARY KEY
     ,idstudent Int
     ,idobjstud Int
     ,data Date
     ,nota Decimal(4,2) CHECK (1<=nota and nota<=10)
  )  
